export type ChatCommand =
  | "preview-run"
  | "confirm-run"
  | "cancel"
  | "repair-run"
  | "retry-run"
  | "status"
  | "approve"
  | "reject"
  | "continue"
  | "apply-graph"
  | "discard-graph"
  | "last-run-summary"
  | "input-summary"
  | null;

/**
 * 一句話同時說「把第 9 步改成…，再測一次」時，具體修改必須先交給建圖／改節點流程；
 * 不能因為句尾有「測」就把整句降格成只執行。先移除「不要修改外部資料」這類安全聲明，避免誤判。
 */
export function hasConcreteWorkflowEditIntent(text: string): boolean {
  const value = text.replace(/\s+/g, " ").trim();
  // 假設性提問(「如果改成X會影響什麼」)是在問後果，不是要求真的去改；反問過去動作
  // (「為什麼你又把X改成Y？」)是在追問AI已經做過的事，也不是新的修改指令。兩者都要在
  // 下面「改成/改為」這種寬鬆比對之前排除，否則會被誤判成具體修改意圖——實測踩過：
  // 「我想知道如果改成業務週會會影響什麼，不要改」與「為什麼你又把月報週會改成業務週會？」
  // 都會被目前的寬鬆規則判定成有修改意圖，導致 phase:"edits" 可能真的被套用。
  if (/如果.{0,60}(?:會|影響).{0,30}(?:什麼|怎樣|如何|多少)/.test(value)) return false;
  if (/(?:為什麼|為何|怎麼).{0,10}又.{0,60}(?:改成|改為|換成|改掉).{0,40}[？?]/.test(value)) return false;
  const withoutSafetyDisclaimer = value.replace(
    /(?:不要|別|不能|不會|不可).{0,8}(?:改動|修改|寫入|更新|動到).{0,10}(?:資料|檔案|試算表|外部內容)?/g,
    "",
  );
  return /(?:把|將).{0,100}(?:改成|改為|換成|改掉|重寫|新增|加上|刪除|移除|接到|填到|寫到)/.test(withoutSafetyDisclaimer) ||
    /(?:請|幫我|我要|我需要|需要).{0,24}(?:修改|調整|更改|改成|改為|改掉|重寫|新增|加上|刪除|移除).{0,100}(?:節點|步驟|流程|欄位|內容|邏輯|設定|資料|填|寫)?/.test(withoutSafetyDisclaimer) ||
    /(?:還要|也要|另外).{0,60}(?:填入|寫入|更新到|新增)/i.test(withoutSafetyDisclaimer) ||
    /(?:第\s*\d+\s*(?:步|節點)|節點\s*\d+).{0,80}(?:改|調整|新增|刪除|重寫)/.test(withoutSafetyDisclaimer) ||
    // 條列式/直接陳述句(如「代碼:X」「檔名也改成:Y」)沒有「把/將/請/幫我」這種完整句型前綴，
    // 但「改成/改為/換成/改掉」本身已經是明確的「換成什麼值」訊號，不強制要求前面有主詞句型
    // (跟 builder.ts 的 isLikelyExistingGraphEdit 同一套放寬理由，兩邊保持一致)。
    /(?:改成|改為|換成|改掉)/.test(withoutSafetyDisclaimer);
}

/**
 * 使用者用「記住」「規則是」「以後都要」這類明確收尾語，要求把某件事當成之後都要遵守的規則——
 * 這是刻意要求持久化保存、優先於一般背景脈絡的明確信號，跟單純描述這次需求不同(2026-07 第三輪
 * 外部審查「沒有穩定的工作流需求規格」這個 P1 的縮小範圍解法：先接住「使用者明確要求記住」
 * 這個最具體的訊號，完整的允許修改欄位白名單/正式驗收條件是更大的架構專案，這裡不做)。
 * 觸發條件刻意保守(只認這幾種明確句型)，避免把一般描述性語句("這條流程要每天早上九點跑")
 * 誤存成永久規則——存錯規則比不存更糟，因為它會被當成「優先於其他脈絡」餵給之後每一次建圖。
 */
export function extractRememberedRule(text: string): string | null {
  const value = text.replace(/\s+/g, " ").trim();
  // 真實踩過的回歸：「如果以後都不要寄信，會影響什麼？」這種純粹在問假設性後果的句子，會被
  // 底下的「以後都不要…」正則整句尾端抓走，變成一條寫死的永久規則(把「使用者在問問題」誤存成
  // 「使用者下的指令」)。任何看起來像提問/純粹好奇的句子都要先排除，寧可漏抓、不能誤存——
  // 存錯規則比不存更糟，因為它會被當成「優先於其他脈絡」餵給之後每一次建圖。
  if (/[?？]/.test(value)) return null;
  if (/如果.{0,60}(?:會|影響).{0,30}(?:什麼|怎樣|如何|多少)/.test(value)) return null;
  if (/我?只是(?:先)?(?:問問|好奇|想(?:先)?了解(?:一下)?|想知道)/.test(value)) return null;
  // 「記住」本身若被否定(不用/不要/別/不需要記住)，代表使用者明講不用存，後面的字面比對
  // 不能因為句子裡剛好出現「記住」兩字就誤判成要記——「我只是問問，不用記住：以後都要寄信」
  // 這種句子曾經被誤存成「以後都要寄信給主管」(真實踩過的回歸)。
  if (/(?:不用|不要|別|不需要)\s*記住/.test(value)) return null;
  const patterns = [
    /(?:請)?記住[，,：:]\s*(.{4,200})$/,
    /規則(?:是[：:]?|[：:])\s*(.{4,200})$/,
    // 這裡要連「要/不要/不能/得」本身一起捕捉進來，不能只留後半句——「以後都不要寄信」如果只
    // 抽出「寄信」，會把「不要做」的規則變成看起來像「要做」的規則，語意整個反過來(真的踩過
    // 才發現的設計錯誤，寫測試時才注意到)。
    /以後(?:都)?((?:要|不要|不能|得)\s*.{2,200})$/,
  ];
  for (const re of patterns) {
    const m = value.match(re);
    const rule = m?.[1]?.trim().replace(/[。.!！]+$/, "");
    if (rule && rule.length >= 4) return rule;
  }
  return null;
}

/**
 * 使用者要求「忘記／取消」之前存下的規則——目前只支援整批清除，不做單條精準比對刪除
 * (規模上單條刪除需要「使用者指哪一條」的消歧機制，先給一個安全、可恢復的最小可行解：
 * 清掉全部規則，使用者可以再重新說一次真正要的規則，好過完全沒有任何刪除/修正管道)。
 */
export function hasForgetRulesIntent(text: string): boolean {
  const value = text.replace(/\s+/g, " ").trim();
  return /(?:忘記|刪除|取消|不用再記|不要再記|清掉)(?:剛剛|剛才|上面|之前|所有|全部)?(?:那條|那個|全部)?規則/.test(value);
}

/**
 * 使用者在同一句話裡，用「不要改／先不要／先別」這類收尾語明確叫停真的套用改動——
 * 常見句型：「如果把A改成B會怎樣？不要改」「刪掉X會怎樣？先不要改」「這個網址能不能用？先不要改」。
 * 這跟 hasConcreteWorkflowEditIntent 是兩回事：後者判斷「這句話本身是不是修改指令」，用來決定
 * 一般模型是否該回 phase:edits；這裡要抓的是「即使判斷出使用者在問/測試某個改動會怎樣，句尾仍
 * 明確叫停」，用來擋確定性快速通道(文字替換/刪節點/Apps Script網址自動寫入)跟一般模型的
 * phase:edits 套用在還沒真的確認前就寫入磁碟(2026-07 第三輪外部審查抓到的 P0：這三條快速
 * 通道跟一般套用完全不看 concreteEditIntent，只要句子裡剛好有替換/刪除/網址就會直接執行)。
 * 只錨定句尾，不擋「不要改成A，要改成B」這種其實還是要改、只是換目標的說法(中間有其他字時
 * 負向詞跟動詞就不相鄰，不會誤判)。
 */
export function hasExplicitEditRefusal(text: string): boolean {
  const value = text.replace(/\s+/g, " ").trim();
  // 「其他都不要動／別的都不要碰」是「除了剛才明確要求的以外，其他都不要動」，語意跟「先不要
  // 動任何東西」(整體叫停剛才那個請求本身)完全相反——真實踩過的回歸(用自己寫的驗收腳本打真實
  // API 才發現)：「把自動執行時間改成每天早上九點就好，其他都不要動」這種話，使用者明明要求
  // 了一項具體修改，卻因為句尾的「其他都不要動」被整句判定成拒絕，連他自己要求的那項修改也
  // 被一起攔下。這個排除必須排在下面的一般判斷之前。
  if (/(?:其他|別的|剩下的)(?:的)?(?:都|全)?(?:不要|不用|別)(?:再)?(?:動|改|修改)/.test(value)) return false;
  // 真實踩過的回歸：verb 清單一度含「執行/存檔/儲存/寫入/更新」，導致「幫我把第8步程式碼修好，
  // 但先不要執行」被誤判成「不要改」——使用者其實是要求「改好、但先別跑」，這兩件事完全不同
  // 「不要執行」該由 wantsPreviewAfterConcreteEdit／chatCommand 的執行語意處理，不該讓編輯本身
  // 被攔下)。這裡只留「改/修改/動/套用」——前三個是明確「不要碰內容本身」的動詞；「套用」在這個
  // 產品的用語裡專指「接受套用某個修改」(如「套用到畫布」)，是編輯語意而非執行語意，跟「執行/
  // 存檔/儲存/寫入/更新」不同——那幾個字既可能指「不要動設定」也可能指「不要真的寫出資料/跑
  // 流程」，語意含糊，刻意不放進來，避免這兩種完全不同的使用者意圖被同一顆正則混在一起。
  const editVerbRefusal = /(?:先)?(?:不要|不用|別|先別|暫時(?:先)?別|不能|不可以)(?:真的|去|直接)?(?:改|修改|動|套用)[^。!！.？?]{0,12}[。!！.]?$/.test(value);
  if (editVerbRefusal) {
    // 「不要改成A，要改成B」其實還是要改，只是換目標——句尾這段若還接著另一次「改成/改為/換成/
    // 換為」的重新指定，代表這不是真的叫停，而是換一個目標繼續要改。
    const tailMatch = value.match(/(?:先)?(?:不要|不用|別|先別|暫時(?:先)?別|不能|不可以)(?:真的|去|直接)?(?:改|修改|動|套用)([^。!！.？?]{0,12})[。!！.]?$/);
    const tail = tailMatch?.[1] ?? "";
    if (!/(?:改成|改為|換成|換為)/.test(tail)) return true;
  }
  // 「我只是問問／只是好奇／單純想知道」這類明講「只是在問、不是要求動手」的說法，即使沒有
  // 「不要」也要視同拒絕——常見句型：「把X改成Y會有什麼影響？我只是先問問」。
  if (/我?只是(?:先)?(?:問問|好奇|想(?:先)?了解(?:一下)?|想知道)/.test(value)) return true;
  // 「還在想／還沒決定／再考慮」這類明講「人還沒下定決心」的猶豫說法，即使句子裡剛好有引號
  // 配對的「A改成B」而觸發了文字替換這類確定性快速通道，也不代表使用者現在就要真的寫入
  // (真實踩過的回歸：「如果把『月報週會』改成『業務週會』…需要跟著改嗎？我還在想要不要換，
  // 先跟我說說看會有什麼影響就好」——這句沒有任何一個「不要」字眼，卻仍是純粹的猶豫探詢)。
  if (/還(?:在)?(?:想|考慮|沒(?:決定|想好))|再(?:考慮|想想|想一下)/.test(value)) return true;
  return false;
}

export function wantsPreviewAfterConcreteEdit(text: string): boolean {
  return hasConcreteWorkflowEditIntent(text) &&
    /(?:再|然後|接著|改完|修改後|調整後|順便).{0,12}(?:測試|試跑|驗證|跑一次|再跑|再試|測一次|試一次)|(?:測試|試跑|驗證).{0,20}(?:修改|調整|改完)/.test(text);
}

/**
 * 對話裡的「測試／跑一次看看」不是建圖需求，而是要拿現有流程做安全試跑。
 * 只吃同時具有測試語意與執行動作的句子，避免「把節點名稱改成測試」之類的一般編輯被誤執行。
 */
export function classifyChatCommand(text: string): ChatCommand {
  const value = text.replace(/\s+/g, " ").trim();
  if (!value) return null;
  if (/(?:名稱|節點|標籤).{0,12}(?:改成|命名為|叫做).{0,8}(?:測試|試跑)/.test(value)) return null;

  // 具體修改意圖必須排在所有非錨定(不要求整句就是那個詞)的規則之前，尤其是 confirm-run／repair-run——
  // 它們的部分規則只找句中片段(例如「數字…沒問題…正式執行」)，一句「把第 9 步改成…，這樣應該沒問題，正式執行」
  // 會先被 confirm-run 的片段規則命中，導致具體修改被整個吞掉、卻拿舊的、未修改的計畫去正式執行
  // (踩過的真實回歸：QA-098 的修復只擋到 preview-run，沒擋到 confirm-run／repair-run)。
  // 前面的排除句(改測試名稱)與下面錨定整句(^…$)的控制命令(停止/狀態/核准…)本身不會與修改意圖衝突，可以留在前面。
  if (hasConcreteWorkflowEditIntent(value)) return null;

  // 控制命令一定要先於「執行」判斷。例如「停止執行」含有執行兩字，但絕不能被當成重新試跑。
  if (/^(?:停止|停下|先停|不要再跑|不要跑了|別跑了|取消執行|取消目前|中止)(?:吧|一下|目前的流程|這次執行|這次測試|現在的工作)?[！!。.]?$/.test(value) ||
      /(?:幫我|請|立刻|馬上).{0,5}(?:停止|中止|取消).{0,8}(?:執行|測試|修復|流程)/.test(value)) return "cancel";
  if (/(?:現在|目前).{0,8}(?:跑到哪|做到哪|進度|狀態)|(?:卡住了嗎|卡在哪|還在跑嗎|完成了嗎|現在怎樣)/.test(value)) return "status";
  if (/(?:目前|現在|這條流程).{0,16}(?:執行時|跑之前).{0,12}(?:要填什麼|會問什麼|可以選|能不能選|有哪些選項|哪些欄位)|(?:執行時|跑之前).{0,12}(?:可以|能不能).{0,8}(?:自己)?(?:選|設定).{0,8}(?:日期|區間|期間|條件)/.test(value)) return "input-summary";
  if (/^(?:直接)?(?:套用(?:到畫布)?|採用這版|使用這版|就照這樣|這版可以|確認這個流程|把這版存下來)[！!。.]?$/.test(value)) return "apply-graph";
  if (/^(?:捨棄(?:這版|這個流程圖)?|不要這版|不用這版|不要這個流程圖|重新來|重來)[！!。.]?$/.test(value)) return "discard-graph";
  if (/(?:剛剛|剛才|上次|上一筆|最近一次).{0,16}(?:做了什麼|執行結果|跑了什麼|結果如何|為什麼失敗|抓到什麼|算出什麼|輸出什麼|哪些資料|用了哪個|用什麼|日期|區間|有沒有寫入|寫了什麼|為什麼沒(?:有)?(?:填|寫|更新)|沒有成功)|(?:哪一步|哪裡).{0,5}(?:失敗|出錯)/.test(value)) return "last-run-summary";
  if (/^(?:核准|批准|同意)(?:這次|繼續|執行)?[！!。.]?$/.test(value)) return "approve";
  if (/^(?:拒絕|不核准|不同意)(?:這次|執行)?[！!。.]?$/.test(value)) return "reject";
  if (/^(?:(?:我)?已經)?(?:填好了|設好了|弄好了|有了|補好了)[！!。.]?$/.test(value) || /^(?:繼續|繼續做|接著做)[！!。.]?$/.test(value)) return "continue";

  // confirm-run 的規則本身已經很精準(不是要求整句錨定、就是要求「數字/結果/預覽」+「沒問題/正確」
  // +「正式執行/寫入/更新」三個具體片語同時出現)，誤觸機率低，排在下面的否定檢查之前——
  // 「數字沒問題，不要測試，正式執行」這種先說不用再測、直接正式執行的自然講法，「不要測試」
  // 短短 4 字內就會命中否定規則，但使用者其實是在明確要求正式執行，不能讓它被否定規則吃掉
  // 變成什麼都不做(code review 抓到的真實回歸)。
  if (/^(?:確認|我確認|確定|可以)(?:，|,|\s)*(?:正式)?(?:執行|寫入|更新)(?:一次)?[！!。.]?$/.test(value) ||
      /(?:數字|結果|預覽).{0,8}(?:沒問題|正確|對了).{0,8}(?:正式執行|寫入|更新)/.test(value)) return "confirm-run";

  // 「不要執行/先不要跑」的否定必須排在 repair-run／retry-run 之前——這兩個命令的部分規則只找
  // 片段(例如「幫我...修好」不要求整句都是修復請求)，「幫我修好，但先不要執行」會先被 repair-run
  // 的片段規則命中而直接觸發自動修復重跑，否定語氣完全沒有機會生效(實測踩過)。回傳 null 讓這則
  // 訊息交給一般對話/建圖路徑處理——那條路只有使用者明確說「改完再測」時才會在套用修改後跑安全
  // 試跑(wantsPreviewAfterConcreteEdit)，單純「先不要執行」不會滿足那個條件，AI 只會把程式碼
  // 修好、不會擅自執行。confirm-run 已經排在這條之前，不受影響。
  if (/(?:不要|不用|先別|取消).{0,4}(?:測試|試跑|執行|跑一次|跑)/.test(value)) return null;

  if (/(?:修到|測到).{0,8}(?:會跑|成功|正確)|(?:自動|繼續|幫我).{0,8}(?:修復|修好).{0,8}(?:再跑|流程|問題)?|(?:失敗|錯誤).{0,8}(?:幫我修|自動修)/.test(value)) return "repair-run";
  if (/^(?:重試|重跑|再跑一次|再試一次|從失敗處繼續|繼續跑)[！!。.]?$/.test(value)) return "retry-run";
  // 「只讀測試整條流程，不要改資料」是最明確的安全試跑；不能因為只讀兩字排在測試前面就掉到一般 AI 建圖。
  if (/(?:只讀|安全).{0,6}(?:測試|試跑|驗證)|(?:測試|試跑|驗證).{0,24}(?:不要|別|不能|不會).{0,8}(?:改動|修改|寫入|更新|動到)/.test(value)) return "preview-run";
  // 非技術使用者最自然會說「看看能不能看到資料，但不要改動」；這就是只讀試跑，
  // 即使句子裡沒有「執行／抓／讀」等工程詞也要聽得懂。
  const explicitReadOnly = /(?:測試|試試|驗證|幫我看).{0,20}(?:看得到|看到|讀得到|抓得到|有沒有).{0,12}(?:資料|內容|檔案|數字).{0,24}(?:不要|不會|別|不能).{0,8}(?:改動|修改|寫入|更新|動到)/.test(value) ||
    /(?:不要|別|不能).{0,8}(?:改動|修改|寫入|更新|動到).{0,24}(?:測試|試試|驗證|看看).{0,20}(?:資料|內容|檔案|數字)/.test(value);
  if (explicitReadOnly) return "preview-run";
  // 「測試」也可能只是名詞（建立一個測試流程／測試環境），不能只看到「測試＋流程」就真的執行。
  // 只接受句首命令、緊鄰的動作詞，或明確的「再測一次／跑一次看看」。
  const explicitTest = /^(?:測試|試跑|驗證)(?:一下|看看|一次|這條流程|結果|能不能跑)?/.test(value) ||
    /(?:幫我|請|去|先|現在|立刻|馬上|想|要|再|重新)\s*(?:測試|試跑|驗證|測(?:試)?一次)(?:一下|看看|一次|這條流程|結果|能不能跑)?/.test(value) ||
    /跑一次看看/.test(value) ||
    /^(?:(?:幫我|請|現在|直接|先)\s*)?(?:用\s*)?(?:\d{1,4}[年/.\-].{0,30})?(?:跑|抓|讀|算)(?:一下|一次|看看|\s|\d)/.test(value) ||
    /^(?:(?:幫我|請|現在|直接|先)\s*)?(?:跑|抓|讀|算).{0,20}(?:上週|本週|上月|本月|上一季|本季|去年|今年|最近\s*\d+\s*天)/.test(value) ||
    /(?:用|拿).{0,35}(?:日期|區間|期間|上週|本週|上月|本月|上一季|本季|最近\s*\d+\s*天|\d{1,4}[年/.\-]).{0,20}(?:跑|抓|讀|算)(?:一下|一次|看看)?/.test(value);
  const checkWrite = /(?:抓|跑|執行)一次.{0,30}(?:看看|確認|檢查|填)|看看.{0,20}(?:能不能|有沒有辦法).{0,20}(?:填|寫).{0,8}(?:對|正確)/.test(value);
  // 即使使用者直接說「現在幫我填／直接執行一次」，有外部副作用也一律先預覽再確認，
  // 不能丟給建圖模型，也不能因為語氣很明確就跳過安全閘門。
  const directExecution = /(?:現在|直接|立刻|馬上).{0,15}(?:跑|執行|抓一次|填|寫入|更新)|(?:幫我|請).{0,10}(?:跑一次|執行一次|填入|寫入)/.test(value);
  const action = /(?:跑|執行|抓|讀|算|填|寫|更新|流程)/.test(value);
  return explicitTest || (action && (checkWrite || directExecution)) ? "preview-run" : null;
}
